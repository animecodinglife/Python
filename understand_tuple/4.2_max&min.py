# max() and min()

'''
When we work with tuple composed of numeric items (including integer and floats) we can use the max () and min ()  functions to find the highest and lowest values contained in the respective tuple..

'''

more_no=(212.32,43.43,432.43,87,432,4324,56464,987,9078)

print(max(more_no))

print(min(more_no))